const frutas = ["Manga", "Laranja", "Tangerina", "Banana"];

console.log("\nArray antes da ordenação:\n");
console.info(frutas);

// Vai ordenar as frutas alfabeticamente
// Obs: No método sort() arrays de string são colocados automaticamente em ordem alfabética 
